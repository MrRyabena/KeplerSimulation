

public class DrawConfig
{
    public float shift_x;
    public float shift_y;
    public float scale_x;
    public float scale_y;
    public float scale_R;

    public DrawConfig(float sh_x, float sh_y, float sc_x, float sc_y, float sc_R)
    {
        shift_x = sh_x;
        shift_y = sh_y;
        scale_x = sc_x;
        scale_y = sc_y;
        scale_R = scale_R;
    }
};
